a, b = map(int, input().split())

print(a + b)                            # 합
print(a - b)                            # 빼기
print(a * b)                            # 곱샘
print(a // b)                           # 몫
print(a % b)                            # 나머지
print(format(float(a / b),'.2f'))       # 나눈 값과 소수점 이하 둘째 자리