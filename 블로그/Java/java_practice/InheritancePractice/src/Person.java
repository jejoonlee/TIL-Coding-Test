public class Person {
    private String name;
    private int age;
    private String nationality;
    private String address;
    private String phoneNumber;

    public Person (String name, int age, String n, String a, String p) {
        this.name = name;
        this.age = age;
        this.nationality = n;
        this.address = a;
        this.phoneNumber = p;
    }
}
