public class StudentWithoutInheritance {
    private String name;
    private int age;
    private String nationality;
    private String address;
    private String phoneNumber;
    private String schoolName;
    private int year;
    private String classNum;

    StudentWithoutInheritance (String name, int age, String n, String a, String p, String sN, int y, String cN) {
        this.name = name;
        this.age = age;
        this.nationality = n;
        this.address = a;
        this.phoneNumber = p;
        this.schoolName = sN;
        this.year = y;
        this. classNum = cN;
    }
}
